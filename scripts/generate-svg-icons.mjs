import { mkdir, readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, "..");
const iconsDir = path.join(projectRoot, "assets/icons/svg");
const outputFile = path.join(projectRoot, 'src/components/ui/icon/app-icon.registry.ts');

const IMPORT_PREFIX = "Icon";

function toPosixPath(value) {
  return value.split(path.sep).join("/");
}

function toKebabCase(value) {
  return value
    .replace(/\.svg$/i, "")
    .replace(/([a-z0-9])([A-Z])/g, "$1-$2")
    .replace(/[^a-zA-Z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .toLowerCase();
}

function toIdentifier(value) {
  const cleaned = value
    .replace(/[^a-zA-Z0-9]+/g, " ")
    .trim()
    .split(/\s+/)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join("");

  return `${IMPORT_PREFIX}${cleaned || "Svg"}`;
}

async function collectSvgFiles(dir, prefix = "") {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = await Promise.all(
    entries.map(async (entry) => {
      const relativePath = path.join(prefix, entry.name);
      const absolutePath = path.join(dir, entry.name);

      if (entry.isDirectory()) {
        return collectSvgFiles(absolutePath, relativePath);
      }

      if (entry.isFile() && entry.name.toLowerCase().endsWith(".svg")) {
        return [relativePath];
      }

      return [];
    }),
  );

  return files.flat().sort((left, right) => left.localeCompare(right));
}

async function main() {
  await mkdir(iconsDir, { recursive: true });

  const svgFiles = await collectSvgFiles(iconsDir);

  const imports = svgFiles.map((relativePath) => {
    const importName = toIdentifier(relativePath);
    const importPath = toPosixPath(
      path.relative(path.dirname(outputFile), path.join(iconsDir, relativePath)),
    );

    return `import ${importName} from "${importPath}";`;
  });

  const entries = svgFiles.map((relativePath) => {
    const iconName = toKebabCase(relativePath.replaceAll(path.sep, "-"));
    const importName = toIdentifier(relativePath);

    return `  "${iconName}": ${importName},`;
  });

  const content = `// This file is auto-generated by \`npm run icons:generate\`.
// Do not edit it manually.
${imports.join("\n")}

export const appIcons = {
${entries.join("\n")}
} as const;

export type AppIconName = keyof typeof appIcons;
`;

  await writeFile(outputFile, content);

  console.log(
    `Generated ${path.relative(projectRoot, outputFile)} with ${svgFiles.length} icon(s).`,
  );
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
